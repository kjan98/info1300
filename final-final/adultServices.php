    <h3 class= "pageTitle">Adult Services</h3>

      <p>Individuals receiving mental health services are among the most vulnerable in our community.
        The nature of their illnesses often results in a variety of peripheral problems ranging from
        well-meaning slights and stigma, to disregard of basic civil rights. Part of the long history
        of psychiatric hospitals and hospitalizations is unfortunately the recurring problem of patient
        abuse and neglect. Although deinstitutionalization has resulted in resolution of many such problems,
        every solution has its own price. Deinstitutionalization caused a new set of problems,
        such as homelessness, lack of adequate follow-up treatment and support, and simple loneliness. The
        MHATC addresses the stigma and discrimination experienced by people with mental health problems.
        Additionally, we advocate for new and improved mental health services locally and at the state level.
      </p>

      <h3 class = "subtitle">What We Provide</h3>
      <ul>
        <li>Information and assistance to people using mental health services and
          to their family and friends
          <li>Help for people to connect with local therapists</li>
          <li>Assistance in coordinating services</li>
          <li>Answers to questions concerning individual rights in different
            mental health environments</li>
            <li>Assistance in resolving problems among people and systems</li>
            <li>Direct assistance to arrange referral to legal or other resources</li>
            <li> Training sessions</li>
          </ul>

          <h3 class = "subtitle">Training</h3>
          <p>Adult Services provides various training sessions to mental health recipients who are residents of Tompkins, Tioga, Schuyler,
            Seneca and Yates Counties. These trainings are free of charge and teach many skills which are
            useful in establishing a more productive and hopeful relationship with the world. The program
            seeks to develop in people a positive, progressive attitude toward personal responsibility.
          </p>

          <p>Adult Advocacy Program of the MHATC has the responsibility of monitoring mental health services
