        <h3 class = "subtitle">Why our cause should matter to all Americans</h3>
        <p>Whether we have a mental illness such as depression, know someone who
           has experienced such a problem or neither, we need to care about the
           issue of mental health. After all, we all have ‘mental health.’ We may
            not think much about our ‘mental health’ or even use that phrase, but
             it’s a common element in all our lives. Some people define it as a
             “state of mind.” Others view it as “being content with life” or “feeling
              good about yourself.” </p>

        <p>‘Mental health’ is perhaps best explained as how well we cope with daily
           life and the challenges it brings. When our ‘mental health’ is good,
            we can deal better with what comes our way — at home, at work, in life.
             When our ‘mental health’ is poor, it can be difficult to function in
             our daily lives. It is a fluid state with disability and untreated illness
              at one end, and recovery and complete wellness at the other end.
              Most of us live and move within the middle range of the spectrum.</p>

        <p>However, most of us take our ‘mental health’ for granted. After all,
          since it’s such a basic, yet unseen, part of who we are, it doesn’t seem
          to merit a lot of thought compared to everything else going on in our lives
          or in the world. But the reality is that ‘mental health’ is a major factor
          in all aspects of each of our lives. We see it play out in our relationships,
          in our performance at work or school and in health issues. </p>

        <p>Today, protecting and strengthening our ‘mental health’ couldn’t be
          more important. With our fast paced, 24/7 culture, we face more stress
          from our daily lives than ever before. Many of us work extended hours
          or multiple jobs, and take less vacation. In fact, one in three American
          employees is chronically overworked. The line between work and home life
           is often blurred, so home is no longer a place of rest. Sleep and
           exercise feel like luxuries. We are eating poorly more often. We are
           constantly bombarded with information. We are also more disconnected
           from family, friends and neighbors, and less engaged in our communities
            than we used to be. Trust in one another has steadily declined over the
            last 30 years. Kids aren’t immune either; many are racing from one
             activity to another without any downtime. </p>

        <p>All of us live with these daily threats to our ‘mental health.’
           Many of us also face additional challenges that test us and put our
            mental health at risk. For some of us, it is the stress of caregiving
            or divorce or losing a loved one. Or losing a job. Or living with a
            disease such as diabetes, cancer or hypertension. Or an addiction to
            alcohol or drugs. Or a major illness such as depression or schizophrenia.
            Or surviving domestic abuse, a street crime or a disaster like Hurricane Katrina. </p>

        <p>Whatever the source of the threat, how able we are to deal with these
          challenges can positively or negatively impact our ‘mental health,’ overall
          health and well-being. When considering all the ways it can affect each of
          us and our society, the issue of ‘mental health’ amounts to the largest public
           health and economic concern in the country.</p>

        <h3 class = "subtitle">What Mental Health America stands for</h3>
        <p> Our message is simple: Good mental health is fundamental to the health
           and well-being of every person and of the nation as a whole.</p>

        <h3 class = "subtitle">Our agenda is clear:</h3>

        <ul class = "agenda">
          <li>We want all people to understand how to protect and improve their
            mental health, and know when to seek help for themselves or someone close to them. </li>
          <li>We want our nation’s schools, businesses, healthcare system and other settings to
            have the knowledge and resources they need to respond to the mental health of their
            constituencies and achieve their missions. </li>
          <li>We want all Americans to have access to high quality, affordable and personalized preventative,
            early-identification and treatment services, when and if the need arises.</li>
          <li>We want persons with disabling mental illnesses to receive the support,
            treatment and services that they need to recover and live full lives in their communities.</li>
          <li>We want more research and services focused on prevention, recovery and cures. </li>
        </ul>

          <p> The good news is — we have the knowledge and experience now about
            what works for good mental health. This know-how has been amply evidenced
            and communicated in a White House conference on mental health, a
            President’s Commission, Surgeon General’s Reports, Institute of Medicine
            reports and other high-level venues. We have tested techniques that
             help children and adults stay resilient to and able to manage threats
             to their mental health. We have effective treatments that combat the
              symptoms of mental illness. We have community programs that help
              people recover more quickly and get back to their lives. We have
              model policies that are working to support long-term resilience,
              recovery and empowerment. What we lack is a national response
              commensurate to the magnitude of the issue. </p>

          <h3 class = "subtitle">Mental Health America will advance its mission by</h3>
          <ul class = "methods">
            <li>Educating the public about ways to preserve and strengthen its mental health; </li>
            <li>Fighting for access to effective care and an end to discrimination
              against people with mental and addictive disorders; </li>
            <li>Fostering innovation in research, practice, services and policy; and </li>
            <li>Providing support to the 60+ million individuals and families living
              with mental health and substance use problems. </li>
          </ul>

          <h3 class = "subtitle">What our new name represents</h3>
          <p>As an organization, Mental Health America has been around for nearly
             a century. We began our work in 1909 when Clifford W. Beers, a young
              businessman who struggled with a mental illness and shared his story
               with the world in his autobiography “A Mind That Found Itself,”
               created a national citizens’ group to promote mental health and
               improve conditions for children and adults living with these health
                problems. It was a revolutionary act and attracted prominent national
                leaders of the time, including the philosopher William James and the
                Rockefeller family. </p>

          <p>Our new name, Mental Health America, was chosen to communicate how
            fundamental mental health is to the overall health and well-being of
            every American. Our new logo is meant to convey Mental Health America
            as a forward-looking, vibrant movement. The bell image in the logo is a
             graphic representation of an actual 300 lb. bell, the Mental Health America Bell.
             The Bell was forged more than 50 years ago with iron chains and shackles
             that bound people in mental asylums. It serves as a vital reminder of our
              past and the progress we have made, and a powerful symbol of our vital mission.
              We invite all Americans to join our movement to help all people live mentally
              healthier lives. To learn more, find help or get involved, go to
              <a class = "links" href = "http://www.mentalhealthamerica.net/"
              target = "_blank">www.mentalhealthamerica.net</a>.</p>

