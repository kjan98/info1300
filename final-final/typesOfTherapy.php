  <div class="textContainer">
    <p>There are many different types of therapy, including those that are most
      effective with families or groups of people. When considering a therapist,
       ask them if they use a particular type of therapy and how it works. You
      may get more out of therapy if you understand how the process usually
      works and how the therapist thinks it will help you. Some therapists will
      blend a few different approaches together to suit your particular needs.
    </p>
      <p>The following are a few common types of therapy:</p>

    <!--columns with types of therapy descriptions-->
    <h3 class = "subtitle">Cognitive-Behavioral Therapy (CBT)</h3>
        <p>This type of therapy has two main aspects. The cognitive part works to develop helpful beliefs
        about your life. The behavioral side helps you learn to take healthier actions. CBT often works
         well for depression, anxiety and bipolar disorder, but it can also be
        used for other various conditions.</p>

      <h3 class = "subtitle">Interpersonal Therapy</h3>
        <p>This type of therapy focuses largely on improving relationships and helping a person express emotions
        in healthy ways. This approach often works well for depression. A variation of it called
        “interpersonal and social rhythm therapy” often works well for bipolar
        disorder because it also helps develop a daily schedule that supports
        recovery.</p>

      <h3 class = "subtitle">Family Therapy</h3>
        <p> Family Therapy helps family members communicate, handle
        conflicts and solve problems better. Forms of family therapy often are
        used for treating eating disorders and bipolar disorder.</p>

      <h3 class = "subtitle">Psychodynamic Therapy</h3>
        <p>This type of therapy helps people develop a better
        understanding about their unconscious emotions and motivations that can
        affect their thoughts and actions.</p>

      <h3 class = "subtitle">Art Therapy</h3>
        <p> Art therapy can include using music, dance, drawing and
        other art forms to help express emotions and promote healing.</p>

      <h3 class = "subtitle">Psychoeducation</h3>
        <p> This type of therapy has the ability to help people understand mental health
        conditions, and it promotes ways to promote recovery.</p>

