  <h3 class = "subtitle">Become a Peer Specialist</h3>

  <p>Peer specialists can do nearly everything concerning mental health from administrative tasks
  to crisis respite.  Peer specialists can do their work in any number of venues:  hospitals, health
  agencies, drop in centers, and free-standing, peer-run agencies.
  </p>

  <p><span class = "bold">All peer specialists in New York State need to meet specific requirements:</span>
     <ul>
        <li>At least 18 years of age</li>
        <li>18 online courses</li>
        <li>Publicly (self-determined) acknowledgement that the individual has been in the past,
            or is now, using mental health services.</li>
    </ul>
  <p>Provisional certification as a Peer Specialist does not require having done 2000 hours of peer
  specialist work.
  </p>

  <p>Training to be a Peer Specialist is currently available at no cost through
  <a class = "links" href = "http://www.academyofpeerservices.org/"
    target = "_blank">The Academy of Peer Services.</a>
  </p>

  <p>The certification can be accomplished on your own, however, you will need a supervisor to
  verify your work and any hours you are stating as tasks of a peer specialist.  Or, you may
  choose to use the Mental Health Association in Tompkins County.  We have computers available and
  supplemental training for peer specialists on Mondays from 10-11 a.m. and Thursdays from 1 – 2 p.m.
  </p>

