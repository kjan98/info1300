<h1 class = "pageTitle">Wellness Recovery Action Plan (WRAP)</h1>

<p>The <a class = "links" href = "http://mentalhealthrecovery.com/" target = "_blank">
Wellness Recovery Action Plan®</a>, or WRAP®, is an evidence-based system that is used world-wide
by people who are dealing with mental health and other kinds of health challenges, and by people who
want to attain the highest possible level of wellness. It was developed by a group of people who
have a lived experience of mental health difficulties; people who were searching for ways to resolve
issues that had been troubling them for a long time. WRAP® involves listing your personal resources,
your Wellness Tools, and then using those resources to develop Action Plans to use in specific
situations which are determined by you. WRAP® is adaptable to any situation. WRAP® also includes a
Crisis Plan or Advance Directive. WRAP® is universal ― it is for anyone, any time, and for any of
life’s challenges.
</p>

<h3 class = "subtitle">Who can benefit from WRAP?</h3>
<p>Anyone who wishes to improve his or her mental health , physical health, maintain wellness,
or simply “check-in” with themselves, may benefit from the Wellness Recovery Action Plan.
WRAP may be used in a variety of settings, such as the workplace, at home, in groups, and so on.
All that is necessary to begin the process of creating a WRAP with a licensed facilitator is a
person’s desire to improve her or his situation and a willingness to set aside some time to
“do the work.”
</p>

<h3 class = "subtitle">Who are facilitators?</h3>
<p>The MHATC has five staff who are credentialed WRAP facilitators. Facilitators are people who
function as “guides,” and are “peers,” meaning that they have experienced the need for and created
a WRAP of their own. Additionally, facilitators have been formally trained to assist others through
the process, initially teaching people how to use the tools of this gentle, non-invasive, self-help
method.
</p>

<h3 class = "subtitle">What kind of time frame are you talking about?</h3>
<p>WRAP may be done in a one-on-one setting, over the course of several days or several hours.
There are options to participate in a weekly WRAP course, a more intensive two day WRAP, or even
the possibility of having a facilitator provide a brief introduction to and explanation of WRAP at
your work setting.
</p>

