<h3><a class = "links" href="typesOfTherapy.php">Types of Therapy</a></h3>
<p>There are many different types of therapy, including those that are most
  effective with families or groups of people. When considering a therapist,
   ask them if they use a particular type of therapy and how it works. You
  may get more out of therapy if you understand how the process usually
  works and how the therapist thinks it will help you. Some therapists will
  blend a few different approaches together to suit your particular needs.
</p>

