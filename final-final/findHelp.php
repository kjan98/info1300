<h3 class = "subtitle">Ready to Make the Call?</h3>
<p>Spend a few minutes talking with him or her on the phone, ask about their approach to working with
patients, their philosophy, whether or not they have a specialty or concentration.
For example, some psychologists specialize in family counseling or child counseling, while others
specialize in divorce or coping with the loss of a loved one.
</p>

<h3 class = "subtitle">The First Meeting</h3>
<p>On your first visit, the counselor or the doctor will want to get to know you and why you called him
or her. The counselor will want to know–what you think the problem is, about your life, what you do,
where you live, with whom you live. It is also common to be asked about your family and friends.
This information helps the professional to assess your situation and develop a plan for treatment.
</p>

<h3 class = "subtitle">What Should I Do if I Don't Feel Comfortable with the Professional?</h3>
<p>If you feel uncomfortable after the first, or even several visits, talk about your feelings at your next
meeting; and don’t be afraid to contact another counselor. Feeling comfortable with the professional you
choose is very important to the success of your treatment!
</p>


