    <h3 class = "subtitle">Which mental health professional is right for me?</h3>
    <p>As you work on your recovery, providers can help you in many ways. They can
      help you to deal with current stress, heal old traumas, make decisions about
      medication and develop treatment plans that reflect your goals. Choosing a mental
      health provider can be challenging, but make sure you match your needs with his or
      her experience and specialty to get the most out of your treatment. </p>

      <p>Over time, many people with mental health conditions say that treatment helps
         them: feel stronger, have a greater ability to cope with symptoms, and develop skills
          for building their relationships and pursuing goals. In fact, most people who seek
          professional help feel better. For example, more than 80 percent of people treated for
          depression improve, and treatment for panic disorders has up to a 90 percent success rate.</p>

      <p>There are different providers who can help you.</p>

      <h3 class = "subtitle">Psychiatrist</h3>
      <p> Psychiatrists are physicians with either a doctor of medicine (M.D.)
         degree or doctor of osteopathy (D.O.) degree, who also has at least four
         additional years of specialized study and training in psychiatry.
          Psychiatrists are licensed as physicians to practice medicine by individual
          states. “Board Certified” psychiatrists have passed the national examination
          administered by the American Board of Psychiatry and Neurology. Psychiatrists
          provide medical and psychiatric evaluations, treat psychiatric disorders,
          provide psychotherapy and prescribe and monitor medications. There are several
          subspecialty boards in psychiatry including child and adolescent, forensic, and addictions. </p>

      <h3 class = "subtitle">Psychologist</h3>
      <p>Psychologists have has a doctoral degree (Ph.D., Psy.D. or Ed.D.) in clinical,
         educational, counseling or research psychology. Psychologists are also licensed
         by individual states to practice psychology. They can provide psychological testing,
          evaluations, treat emotional and behavioral problems and mental disorders,
          and provide a variety of psychotherapeutic techniques.</p>

      <h3 class = "subtitle">Nurse Practitioner</h3>
      <p>Psychiatric/mental health nurse practitioners (PMHNP) have a four-year
        college degree in nursing (BSN) and also complete an approved masters of
        science in nursing (MSN) or doctor of nursing practice (DNP). PMHNPs are
        licensed by individual states and in some states are required to practice
        under the supervision of a psychiatrist. PMHNPs provide a wide range of
        services to adults, children, adolescents and their families including
        assessment and diagnosis, prescribing medications and providing therapy
         for individuals with psychiatric disorders or substance abuse problems.</p>

      <h3 class = "subtitle">Social Worker </h3>
      <p>Social workers have either a bachelor’s degree (B.A., B.S. or B.S.W.),
         a master’s degree (M.A., M.S., M.S.W. or M.S.S.W), or doctoral degree
         (D.S.W. or Ph.D.). In most states, social workers take an examination
         to be licensed to practice social work (L.C.S.W. or L.I.C.S.W.), and the
          type of license depends on their level of education and practice experience.
           Social workers provide a range of services based on their level of
           training and certification. Typically a bachelor’s level social worker
            provides case management, inpatient discharge planning services,
            placement services and a variety of other daily living needs services
            for individuals. Master’s level social workers can provide this level
             of services but are also able to provide assessment and treatment of
              psychiatric illnesses including psychotherapy.</p>

        <h3 class = "subtitle">Licensed Marriage and Family Therapist</h3>
        <p> Licensed marriage and family therapists (LMFT or MFCC) have a graduate
          academic degree (master’s or doctoral degree), clinical work experience
           and has passed a state-certified licensing exam. Most states offer this
           license. Along with the two- to three-year full-time masters programs
           with a practicum and internship, LMFTs are required to complete 1,000
           hours of individual or family therapy with 100 hours of supervision.
            Counselor with a masters degree, with special education and training
            in marital and family therapy. Trained to diagnose and provide individual
            and group counseling.</p>

        <h3 class = "subtitle">Pastoral Counselor</h3>
        <p>Clergy with training in clinical pastoral education. Trained to diagnose and provide individual and group counseling.</p>

