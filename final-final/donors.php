         vulnerable, they are very prone to adopting unhealthy coping mechanisms
         to reduce their negative feelings. At MHATC, it’s our goal to provide
         adolescents with the wellness tools they need to reduce anxiety, identify
         triggers, and avoid unhealthy coping mechanisms. We’re also working very h
         ard to help young people to disassociate seeking help with weakness. Our <span class = "bold">Youth Services Program</span>
