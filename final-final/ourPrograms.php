<h3><a class = "links" href="youthServices.php">Youth Services</a></h3>
<p>The Youth Services Program offers flexible, creative programming to meet the needs of young
people with emotional, developmental, or behavioral challenges. By providing opportunities for
support, socialization, and recreation, the program works to keeps youth living happy, healthy
lives in their homes and communities.
</p>

<h3><a class = "links" href="familyServices.php">Family Support Services</a></h3>
<p>The Family Support Services Program is focused on the needs of families who have a child or
children with a social, emotional, behavioral, developmental or a mental health disability.
Family Support Services provides services to support and empower families so they can improve
the quality of their lives and help their children to achieve their full potential within
the family network and community.
</p>

<h3><a class = "links" href="peerTraining.php">Peer Support and Peer Training Services</a>
</h3>
<p>Peer support programs provide an opportunity for consumers who have achieved significant
recovery to assist others in their recovery journeys. Peer specialists model recovery, teach
skills and offer supports to help people experiencing mental health challenges lead meaningful
lives in the community.
</p>

<h3><a class = "links" href="jenkinsCenter.php">The Jenkins Center for Hope and Recovery</a></h3>
<p>The Jenkins Center for Hope and Recovery is a drop-in center for those who use or have used
mental health services. The Jenkins Center provides a safe environment where mental health
consumers can relax, have fun with peers, and share information and support. The center is unique
because it is staffed by peers, not healthcare professionals or government administrators.
</p>


