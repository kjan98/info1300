<h1 class = "pageTitle">Family Support Services</h1>

<p>The Family Support Services Program is focused on the needs of families who have a child or
   children with a social, emotional, behavioral, developmental or a mental health disability.
   provides services to support and empower families so they can improve the quality of their lives
   and help their children to achieve their full potential within the family network and community.
   Family Support Services provides a wide range of no cost services to help support the entire family.
   </p>

<h3 class = "subtitle">Family Peer Support Services</h3>
    <p>A Family Peer Advocate works across a variety of systems to provide peer support, education,
    advocacy and information and referral. A Family Peer Advocate will work closely with parents/
    guardians in a strength-based relationship to enhance family skills to promote positive youth
    functioning and their child’s ability to be successful. A Family Peer Advocate will be available
    to collaboratively work with and on behalf of families to assist them to obtain needed services
    and supports to promote positive outcomes. The most important advocacy concept of Family
    Support Services is that of being “Effective Parent-Professional-System partnerships."
    </p>

   <p>Family Support Services provides waiver contracts services through the Office of Mental Health
   providing skill-building and respite services.
   </p>


<h3 class = "subtitle">Parent Support Group</h3>
    <p>Parent–driven support groups include parents/guardians and a Family Support Services peer
    advocate who share similar experiences. The Group provides a forum where parents can exchange
    information, share past experiences and create positive techniques and skills to enhance their
    family unity. Family Support Services schedules a variety of educational workshops which have
    included such topics as: Mental health assessments, transition services, special education laws,
    nutrition and a medication presentation by an area pharmacist.
    </p>

<h3 class = "subtitle">Parent Empowerment Training (PEP)</h3>
   <p>The Family Peer Advocate provides training for parents/guardians of children with mental
   health needs so they can gain specific information about mental health disorders and their
   treatments, the mental health care system and services and school system options.
  </p>

