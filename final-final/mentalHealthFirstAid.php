<h3 class = "subtitle">Mental Health First Aid Matters.</h3>
<p>Most of us would know how to help if we saw someone having a heart attack—we’d start CPR, or at
the very least, call 9-1-1. But too few of us would know how to respond if we saw someone having a
panic attack or if we were concerned that a friend or co-worker might be showing signs of
alcoholism.
</p>

<p>Mental Health First Aid takes the fear and hesitation out of starting conversations about
mental health and substance use problems by improving understanding and providing an
<a class = "links" href = "https://www.mentalhealthfirstaid.org/take-a-course/what-you-learn/"
target = "_blank">action plan</a> that teaches people to safely and responsibly identify and
address a potential mental illness or substance use disorder.
</p>

